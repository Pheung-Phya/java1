
package Inheritance_;

import java.util.Scanner;

public class Single {
    String name,unit;

    public Single() {
    }
    public Single(String name, String unit) {
        this.name = name;
        this.unit = unit;
    }
    public String toString() {
        return "Single{" + "name=" + name + ", unit=" + unit + '}';
    }
    
    
}
