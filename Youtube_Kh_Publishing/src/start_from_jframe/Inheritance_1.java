
package start_from_jframe;

public class Inheritance_1 {
    int a;
    float b;
    char sex;

    public Inheritance_1() {
    }

    public Inheritance_1(int a, float b, char sex) {
        this.a = a;
        this.b = b;
        this.sex = sex;
    }

    @Override
    public String toString() {
        return "Inheritance_1{" + "a=" + a + ", b=" + b + ", sex=" + sex + '}';
    }
    
    
}
